package DemoSchool.exception.service;

public class SchoolServiceException extends Exception {

	public SchoolServiceException() {
		super();
	}

	public SchoolServiceException(Throwable arg0) {
		super(arg0);
	}

	public SchoolServiceException(String arg0) {
		super(arg0);
	}

	public SchoolServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
